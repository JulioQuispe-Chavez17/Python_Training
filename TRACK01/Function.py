#COMMNET
import turtle

cesar_turtle = turtle.Turtle()
def square():
    cesar_turtle.forward(100)
    cesar_turtle.right(90)
    cesar_turtle.forward(100)
    cesar_turtle.right(90)
    cesar_turtle.forward(100)
    cesar_turtle.right(90)
    cesar_turtle.forward(100)

square()
cesar_turtle.forward(200)
square()
cesar_turtle.forward(200)
